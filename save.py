def main():
    # Initialize PyAudio
    for i in abnormal_messagesi:
        message = i
        audio = pyaudio.PyAudio()
        OUTPUT_FILE=generate_unique_filename(message)
        print(message)
        x=input("waiting for input")
        # Create a list to store recorded audio data
        frames = []

        try:
            # Open input stream for recording
            stream_input = audio.open(format=FORMAT,
                                    channels=CHANNELS,
                                    rate=RATE,
                                    input=True,
                                    frames_per_buffer=CHUNK)

            # Open output stream for playback
            stream_output = audio.open(format=FORMAT,
                                    channels=CHANNELS,
                                    rate=RATE,
                                    output=True,
                                    frames_per_buffer=CHUNK)

            print("Recording and playing audio in real-time. Speak now!")

            # Record and playback loop
            for _ in range(0, int(RATE / CHUNK * RECORD_SECONDS)):
                try:
                    # Read audio from the input stream
                    audio_data = stream_input.read(CHUNK, exception_on_overflow=False)
                    
                    # Append the audio data to frames for saving
                    frames.append(audio_data)

                    # Write audio to the output stream
                    stream_output.write(audio_data)
                except IOError as e:
                    print(f"Stream error: {e}")
                    break

        finally:
            # Stop and close streams
            stream_input.stop_stream()
            stream_input.close()
            stream_output.stop_stream()
            stream_output.close()

            # Terminate PyAudio
            audio.terminate()

            print("Recording and playback finished.")

            # Save the audio data to a WAV file
            print(f"Saving recorded audio to {OUTPUT_FILE}...")
            with wave.open(OUTPUT_FILE, 'wb') as wf:
                wf.setnchannels(CHANNELS)
                wf.setsampwidth(audio.get_sample_size(FORMAT))
                wf.setframerate(RATE)
                wf.writeframes(b''.join(frames))
            print(f"Audio saved successfully as {OUTPUT_FILE}.")

