def generate_unique_filename(base_name, extension=".wav"):
    """Generates a unique filename by appending a number if the file exists."""
    if not os.path.exists(f"{base_name}{extension}"):
        return f"{base_name}{extension}"
    
    counter = 1
    while os.path.exists(f"{base_name}_{counter}{extension}"):
        counter += 1
    return f"{base_name}_{counter}{extension}"
